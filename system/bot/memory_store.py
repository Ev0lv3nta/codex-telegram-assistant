def ensure_memory_scaffold(assistant_root: Path) -> list[Path]:
    memory_dir = assistant_root / MEMORY_DIR_NAME
    memory_dir.mkdir(parents=True, exist_ok=True)

    created_or_updated: list[Path] = []
    for filename, content in MEMORY_FILE_CONTENT.items():
        target = memory_dir / filename
        if target.exists():
            continue
        target.write_text(content, encoding="utf-8")
        created_or_updated.append(target)
    return created_or_updated


